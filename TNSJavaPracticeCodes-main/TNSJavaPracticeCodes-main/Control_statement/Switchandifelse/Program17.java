class Switchandifelse{
        public static void main(String[]args){

                int x=2000;
			switch(x){

				case 2000:
                                System.out.println("for budget "+x+" destination is mahabaleshwar");
				break;
                        
				case 6000:
                                System.out.println("for budget "+x+" destination is Amritsar");
				break;
                     

				case 10000:
                                System.out.println("for budget "+x+" destination is Manali");                        		break;


				case 15000:
                                System.out.println("for budget "+x+" destination is Jammu and kashmir");
				break;
    


    				default:
                                System.out.println(x+" for other budget try next time ");
                        
        }
}
}
