class Switchandifelse{
	public static void main(String[]args){

		int x=0;
			if(x%2==0&&x>=1){
				System.out.println(x +" is an even number");

			}
			else if(x%2==1){
				System.out.println(x +" is an odd number");

			}
		      	else{
				System.out.println(x +" is an invalid input");
			}
	}
}
