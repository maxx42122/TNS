class Switchandifelse{
        public static void main(String[]args){

                char ch='M';


                switch(ch){


                        case 'S':
                                System.out.println(" Small");
                                break;

                        case 'M':
                                System.out.println(" Medium");
                                break;

                        case 'L':
                                System.out.println(" Large");
                                break;

                        default:

                                System.out.println(" extra Large");
                        }
        }
}
                        
