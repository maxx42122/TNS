class WhileDemo{
        public static void main(String[]args){

                int num=1;
                char ch='A';

                while(num<=6){
			if(num%2==0){
				System.out.print(num+" ");
			}


			else{

                        System.out.print(ch +" ");
			}
                        ch++;


                        num++;
                }
        }
}
