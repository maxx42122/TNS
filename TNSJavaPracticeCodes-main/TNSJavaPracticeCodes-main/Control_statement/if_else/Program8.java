class Divisible{
        public static void main(String[]args){
                int x=10;

                if( x%2==0||x%5==0||x%10==0){
                        System.out.println(x+":is divisible by 2,5,10");

                }
                else{
                        System.out.println(x+":is not divisible by 2,5,10");

                }
        }
}
