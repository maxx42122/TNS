class Pythagorean{
	public static void main(String[]args){

		int x = 53;
		int y = 43;
		int z = 5;

		if(x*x+y*y ==z*z){
			System.out.println(x +" ,"+y+" ,"+z+"  its ppythagorean triplet");
		}
		
		else{
			System.out.println(x +" ,"+y+" ,"+z+"  not a pythagorean triplet");
		}
	}
}

















